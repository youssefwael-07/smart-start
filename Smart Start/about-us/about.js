// Loading animation
window.addEventListener('load', () => {
    document.body.classList.add('loaded');
    
    // Animate hero elements
    const heroTitle = document.querySelector('.hero-title');
    const heroDescription = document.querySelector('.hero-description');
    const heroButtons = document.querySelector('.hero-buttons');
    
    setTimeout(() => {
        if (heroTitle) heroTitle.style.animation = 'slideInUp 0.8s ease forwards';
    }, 200);
    
    setTimeout(() => {
        if (heroDescription) heroDescription.style.animation = 'slideInUp 0.8s ease forwards';
    }, 400);
    
    setTimeout(() => {
        if (heroButtons) heroButtons.style.animation = 'slideInUp 0.8s ease forwards';
    }, 600);
});

